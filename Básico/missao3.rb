# Crie um programa que que receba dois números inteiros e no final exiba a soma, subtração, adição e divisão entre eles.

print "Digite o número 1: "
num1 = gets.chomp.to_f

print "Digite o número 2: "
num2 = gets.chomp.to_f

puts "A soma dos dois é #{num1 + num2}"
puts "A subtração dos dois é #{num1 - num2}"
puts "A multiplicação dos dois é #{num1 * num2}"
puts "A divisão dos dois é #{num1 / num2}"

